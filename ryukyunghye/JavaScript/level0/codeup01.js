// 1. 기초 - 출력

// [1] Hello 출력하기
console.log("Hello");

// [2] Hello World 출력하기
console.log("Hello World");

// [3] Hello World_두 줄에 걸쳐 줄을 바꿔 출력하기
console.log("Hello\n\
World");

// [4] 'Hello' (단, 작은 따옴표도 함께 출력한다.)
console.log("'Hello'");
console.log("'Hello'");
console.log(`'Hello'`);

// [5] "Hello World" (단, 큰 따옴표도 함께 출력한다.)
console.log('"Hello World"');
console.log(`"Hello World"`);

// [6] "!@#$%^&*()" (단, 큰 따옴표도 함께 출력한다.)
console.log(`"!@#$%^&*()"`);

// [7] "C:\Download\hello.cpp" (단, 큰 따옴표도 함께 출력한다.)
console.log(`"C:\\Download\\hellp.cpp`);

/* [8] ┌┬┐
       ├┼┤
       └┴┘ 출력하기*/
console.log("┌┬┐\n\
├┼┤\n\
└┴┘");
